package ModelLojinha;

public class ModelarEstoque {
	
	private int idEstoqueProduto;
	private String nomeEstoqueProduto;
	private int quantidadeEstoqueProduto;
	
	
	public int getIdEstoqueProduto() {
		return idEstoqueProduto;
	}
	public void setIdEstoqueProduto(int idEstoqueProduto) {
		this.idEstoqueProduto = idEstoqueProduto;
	}
	public String getNomeEstoqueProduto() {
		return nomeEstoqueProduto;
	}
	public void setNomeEstoqueProduto(String nomeEstoqueProduto) {
		this.nomeEstoqueProduto = nomeEstoqueProduto;
	}
	public int getQuantidadeEstoqueProduto() {
		return quantidadeEstoqueProduto;
	}
	public void setQuantidadeEstoqueProduto(int quantidadeEstoqueProduto) {
		this.quantidadeEstoqueProduto = quantidadeEstoqueProduto;
	}
}
